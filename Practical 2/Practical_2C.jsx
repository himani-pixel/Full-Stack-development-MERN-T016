//Arrow symbol
let add = (a , b) =>{
   return a + b;
};


let sub = (a , b) => {
   return a - b;
};
let multiply = (a , b) => {
   return a * b;
};


let div = (a , b) => {
   return a / b;
}


console.log("Addition: ",add(10 , 20));
console.log("Subtraction: ",sub(20 , 10));
console.log("Multiplication: ",multiply(5 , 6));
console.log("Division: ",div(10 , 2));
